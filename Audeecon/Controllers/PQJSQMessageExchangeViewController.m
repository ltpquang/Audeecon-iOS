#pragma mark - Message Exchange delegates
- (void)didReceiveMessage:(NSDictionary *)messageContent {
    [self scrollToBottomAnimated:YES];
    NSString *msg = [messageContent valueForKey:@"msg"];
    NSString *sender = [messageContent valueForKey:@"sender"];
    [_messages addObject:[[JSQMessage alloc] initWithSenderId:sender senderDisplayName:@"test name" date:[NSDate distantPast] text:msg]];
    [self finishReceivingMessageAnimated:YES];
    //[_messages addObject:[messageContent valueForKey:@"msg"]];
}

